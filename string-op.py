# Basic string operations
str1="Hello"
str2="World"

# '+' - concatenation operator
print(str1+str2)

# '*' - Repetition operator
print(str2*2)

# [] - slice operator
print(str1[3])

#[:]-Range operator
print(str1[2:4])

# in - Membership operator
print('H' in str1)

# not in - Membership operator
print('A' not in str1)

# r/R - Raw string -print r'\n' prints \n and print R'\n' prints \n
print(r'\n')
print(R'\n')